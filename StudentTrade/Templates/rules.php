<h1>Regler kring annonsering</h1>

	StudentTrades regler kring annonsering innebär att man inte får använda StudentTrade.se för att sprida information strider mot gällande lagstiftning. Vi förbehåller oss också rätten att utan varning ta bort annonser som innehåller rasism, sexism, antidemokratisk propaganda och annat som kan upplevas kränkande. 

<h2>Skäl för borttagning av annons</h2>

<h3>Fel kontaktuppgifter</h3>
Om en person indentifieras med namn, telefonnummer, e-post eller liknande kommer annonsen tas bort om det framkommer att den personen inte vill ha den informationen i annonsen, i enlighet med personuppgiftslagen.

<h3>Fel innehåll för kategorin</h3>
Om en annons är placerad i fel kategori kan annonsen kommas att tas bort.

<h3>Otillåtet innehåll</h3>
Innehåll som strider mot gällande lagstiftning, antidemokratisk propaganda, rasism, sexism tas bort.

<h3>Otillåten formulering</h3>
Om en annons formulerats på ett oacceptablet sätt (t.ex rasism, sexism, för grovt språk eller övrigt språk som kan anses kränkande) kommer den tas bort.
